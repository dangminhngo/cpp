#include <iostream>

int main() {
  auto k{430u};
  auto j{(k >> 4) & ~(~0u << 3)};
  std::cout << j << std::endl;

  /* My solution
   Original k   = 0000'0000'0000'0000'0000'0001'1010'1110
   k >> 4       = 0000'0000'0000'0000'0000'0000'0001'1010
   ~0u          = 1111'1111'1111'1111'1111'1111'1111'1111
   ~0u << 3     = 1111'1111'1111'1111'1111'1111'1111'1000
   ~(~0u << 3)  = 0000'0000'0000'0000'0000'0000'0000'0111
   j            = 0000'0000'0000'0000'0000'0000'0000'0010 -> 2
  */
}
